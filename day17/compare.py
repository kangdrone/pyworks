# 비교, 논리 연산자
x = 10
y = -10

# 비교 연산자
print(x > 0)  #True
print(y > 0)  #False
print()

print(x > y)  #True
print(x < y)  #False
print()

print(x == 10)  #True
print(x == y)  #False
print(x != y)  #True
print(x is y)  #False
print(x is not y)  #True
print()

#논리 연산자
print(x > 0 and y > 0)  #False
print(x > 0 or y > 0)  #True
print(not y > 0)  #True

